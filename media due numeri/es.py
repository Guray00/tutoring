# prendo in input il primo numero
x=int( input("inserisci il primo numero: ") )

# prendo in input il secondo numero
y= int(input("inserisci il secondo numero: "))

# se i numeri sono uguali:
if x == y:
  # stampo il primo numero
  print(x)

# altrimenti:
else: 
  # calcolo la media tra i due
  media=(x+y)/2

  # stampo la media
  print(media)


